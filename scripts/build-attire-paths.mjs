// Regenerates src/components/attire-paths.ts from attire-piece.svg (root folder).
// The source SVG is a flat image-trace export (196 ungrouped paths). This script:
//   1. bakes each path's translate() into its coordinates and rounds to integers
//   2. classifies every path into one of the 7 attire pieces by its bbox centre
//   3. records each path's brightness offset from its piece's base colour,
//      so the component can recolour a piece while keeping the traced shading
// Run with: node scripts/build-attire-paths.mjs
import { readFileSync, writeFileSync } from 'fs'

const svg = readFileSync('attire-piece.svg', 'utf8')
const re = /<path\b([^>]*)\/>/g

const paths = []
let m
while ((m = re.exec(svg))) {
  const attrs = m[1]
  const d = attrs.match(/ d="([^"]+)"/)[1]
  const fill = attrs.match(/fill="([^"]+)"/)[1]
  const tr = attrs.match(/translate\((-?[\d.]+),(-?[\d.]+)\)/)
  const tx = tr ? Number(tr[1]) : 0
  const ty = tr ? Number(tr[2]) : 0

  // bake translate into coords, round to integers (canvas is 1402x1122 so
  // sub-pixel precision is invisible), and minify the path data
  let isX = true
  let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity
  const baked = d.replace(/-?\d+(\.\d+)?/g, (n) => {
    const v = Math.round(Number(n) + (isX ? tx : ty))
    if (isX) { minX = Math.min(minX, v); maxX = Math.max(maxX, v) }
    else { minY = Math.min(minY, v); maxY = Math.max(maxY, v) }
    isX = !isX
    return String(v)
  })
    .replace(/ ([MCZ])/g, '$1')
    .replace(/([MCZ]) /g, '$1')
    .trim()

  paths.push({
    d: baked, fill,
    cx: (minX + maxX) / 2, cy: (minY + maxY) / 2,
    area: (maxX - minX) * (maxY - minY),
  })
}

// full-canvas background rectangle: drop it
const content = paths.filter((p) => p.area < 1402 * 1122 * 0.9)

function classify({ cx, cy }) {
  if (cx < 600) {
    if (cy < 145) return 'songkok'
    if (cy < 600) return 'groomBaju'
    return cx < 400 ? 'sampin' : 'seluar'
  }
  if (cy < 455) return 'tudung'
  if (cx >= 1030 && cy < 615) return 'tudung' // tail of the veil
  if (cx >= 1070) return 'kain'
  return 'brideBaju'
}

const brightness = (hex) => {
  const n = parseInt(hex.slice(1), 16)
  return (((n >> 16) & 255) + ((n >> 8) & 255) + (n & 255)) / 3
}

for (const p of content) p.piece = classify(p)

// the hijab face opening is the page background showing through — keep it fixed
for (const p of content) {
  if (p.piece === 'tudung' && brightness(p.fill) >= 248) p.piece = 'fixed'
}

// base colour per piece = lightest fill among the piece's large paths
// (each piece has a light garment-fill layer and a dark outline layer of
// similar bbox size; the light one is the garment's true colour)
const base = {}
for (const piece of ['songkok', 'groomBaju', 'sampin', 'seluar', 'tudung', 'brideBaju', 'kain']) {
  const ps = content.filter((p) => p.piece === piece)
  const maxArea = Math.max(...ps.map((p) => p.area))
  const big = ps.filter((p) => p.area >= maxArea * 0.5)
  base[piece] = big.reduce((a, b) => (brightness(a.fill) >= brightness(b.fill) ? a : b)).fill
}

const entries = content.map((p) => {
  if (p.piece === 'fixed') return { k: 'fixed', s: 0, d: p.d }
  return { k: p.piece, s: Math.round(brightness(p.fill) - brightness(base[p.piece])), d: p.d }
})

const counts = {}
for (const e of entries) counts[e.k] = (counts[e.k] ?? 0) + 1
console.log('base colours:', base)
console.log('path counts:', counts)

const out = `// Generated by scripts/build-attire-paths.mjs from attire-piece.svg — do not edit by hand.
// Each path belongs to one attire piece; \`shade\` is its brightness offset from
// the piece's base colour, so recolouring a piece keeps the traced shading.
export type AttirePieceKey =
  | 'songkok'
  | 'groomBaju'
  | 'sampin'
  | 'seluar'
  | 'tudung'
  | 'brideBaju'
  | 'kain'
  | 'fixed'

export type AttirePath = { piece: AttirePieceKey; shade: number; d: string }

export const VIEW_BOX = '70 30 1212 1066'

export const ATTIRE_PATHS: AttirePath[] = [
${entries.map((e) => `  { piece: '${e.k}', shade: ${e.s}, d: ${JSON.stringify(e.d)} },`).join('\n')}
]
`
writeFileSync('src/components/attire-paths.ts', out)
console.log(`wrote src/components/attire-paths.ts (${Math.round(out.length / 1024)} KB, ${entries.length} paths)`)
